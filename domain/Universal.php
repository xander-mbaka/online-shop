<?php
interface Fractal
{
	/** A fractal is a never-ending pattern. Fractals are infinitely complex patterns that are self-similar across different scales. 
	* They are created by repeating a simple process over and over in an ongoing feedback loop. Driven by recursion, fractals are 
	* images of dynamic systems – the pictures of Chaos
	* A fractal is an object or quantity that displays self-similarity, in a somewhat technical sense, on all scales. The object 
	* need not exhibit exactly the same structure at all scales, but the same "type" of structures must appear on all scales.
	*/
}

abstract class Universal implements Fractal
{
	/** 
	* Represents the universal single system of nature, universal one, or universal fractal
	*/
}

?>